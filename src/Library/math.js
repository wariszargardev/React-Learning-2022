export {
    addTwoNumber,
    subTwoNumber,
    multiplyTwoNumber,
    divideTwoNumber
}
const addTwoNumber= (n1, n2) =>{
    return n1 + n2
}

const subTwoNumber= (n1, n2) =>{
    return n2 - n1;
}

const multiplyTwoNumber= (n1, n2) =>{
    return n1 * n2
}

const divideTwoNumber= (n1, n2) =>{
    return n2 / n1
}

